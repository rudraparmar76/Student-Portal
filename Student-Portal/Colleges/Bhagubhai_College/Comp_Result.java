    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        PreparedStatement ps;
        ResultSet rs;
        String uname = jTextField1.getText();
        String roll = jTextField2.getText();

        String query = "SELECT * FROM `marks` WHERE `Name` =? AND `RollNo` =?";

        try {
            ps = StudentMarks.getConnection().prepareStatement(query);

            ps.setString(1, uname);
            ps.setString(2, roll);

            rs = ps.executeQuery();

            if (rs.next()) {
                String student_name = rs.getString(1);
                String College = rs.getString(2);
                String course = rs.getString(3);
                String RollNo = rs.getString(4);
                String Science = rs.getString(5);
                String Maths = rs.getString(6);
                String PRJ = rs.getString(7);
                String Linux = rs.getString(8);
//                String total = (Science+Maths+PRJ+Linux);
                int scienceMarks = Integer.parseInt(Science);
                int mathsMarks = Integer.parseInt(Maths);
                int prjMarks = Integer.parseInt(PRJ);
                int linuxMarks = Integer.parseInt(Linux);

                int totalMarks = scienceMarks + mathsMarks + prjMarks + linuxMarks;
                int maxMarksPerSubject = 100; // Assuming each subject has a maximum of 100 marks
                int totalMaxMarks = maxMarksPerSubject * 4; // Total maximum marks for all subjects

                double percentage = ((double) totalMarks / totalMaxMarks) * 100;
                String total = String.valueOf(totalMarks);
                String percentageString = String.format("%.2f", percentage); // Format the percentage to two decimal places
                jTextField3.setText(student_name);
                jTextField4.setText(RollNo);
                jTextField5.setText(College);
                jTextField6.setText(course);
                jTextField8.setText(Science);
                jTextField7.setText(Maths);
                jTextField10.setText(PRJ);
                jTextField9.setText(Linux);
                jTextField12.setText(total);
                jTextField11.setText(percentageString + "%");
//                   CSE_Marks mf = new CSE_Marks();
//                    mf.setVisible(true);
//                    mf.pack();
//                    mf.setLocationRelativeTo(null);
////                    mf.setExtendedState(JFrame.MAXIMIZED_BOTH);
//                 
//                    this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Incorrect Name Or Roll Number", "Login Failed", 2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Comp_Result.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

